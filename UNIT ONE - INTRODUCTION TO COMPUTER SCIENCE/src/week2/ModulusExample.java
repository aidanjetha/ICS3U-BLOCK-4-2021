package week2;

public class ModulusExample {
    public static void main(String[] args) {
      

        double len1 = 1.1;

        double len2 = 3.2;
        
        double len3 = 2;
        
        double total = len1 + len2 + len3;
        
        int minLength = (int) (total + 1.5);
        
        System.out.print(minLength);


    }
}
